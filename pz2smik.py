#Функция для нахождения количества секунд, прошедших с начала последней минуты
def seconds_since_last_minute(N):
    # Проверяем, что N является неотрицательным целым числом
    if N < 0:
        print ("Ошибка: N должно быть неотрицательным целым числом.")
        return

    #Находим количество секунд в последней минуте
    seconds_in_minute = 60

    # Вычисляем количество секунд, прошедших с начала последней минуты
    seconds_passed = N % seconds_in_minute

    #Возвращаем результат
    return seconds_passed
# Пример использования функции
try:
    N = int(input("Введите количество секунд, прошедших с начала суток (целое число):"))
    result = seconds_since_last_minute(N)

    # Выводим результат, если он корректен
    if result is not None:
        print("Количество секунд, прошедших с начала последней минуты:", result)
except ValueError:
    print("Ошибка: Пожалуйста, введите целое число.")